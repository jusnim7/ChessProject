package piecespackage;

public class ChessPieces {
	
	private static final int king = 7;
	private static final int queen = 6;
	private static final int bishop = 5;
	private static final int knight = 4;
	private static final int rook = 3;
	private static final int pawn = 2;
	
	public static int getKing() {
		return king;
	}
	public static int getQueen() {
		return queen;
	}
	public static int getBishop() {
		return bishop;
	}
	public static int getKnight() {
		return knight;
	}
	public static int getRook() {
		return rook;
	}
	public static int getPawn() {
		return pawn;
	}
	
	

}
